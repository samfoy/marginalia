# ── ANSI colours (disabled on Windows or when not a tty) ─────────────────────

_COLOUR = sys.stdout.isatty() and platform.system() != "Windows"

def _c(code: str, text: str) -> str:
    return f"\033[{code}m{text}\033[0m" if _COLOUR else text

def green(t):  return _c("32", t)
def yellow(t): return _c("33", t)
def cyan(t):   return _c("36", t)
def bold(t):   return _c("1",  t)
def dim(t):    return _c("2",  t)

# ── Helpers ───────────────────────────────────────────────────────────────────

CONFIG_FILE = Path.home() / ".marginalia.env"
BRIDGE_DIR  = Path(__file__).parent


def _ask(prompt: str, default: str = "") -> str:
    hint = f" [{default}]" if default else ""
    try:
        val = input(f"  {prompt}{hint}: ").strip()
    except (EOFError, KeyboardInterrupt):
        print("\nAborted.")
        sys.exit(0)
    return val or default


def _pick(prompt: str, options: list[tuple[str, str]], default: int = 1) -> int:
    """Numbered menu. Returns 1-based index of the choice."""
    print(f"\n  {prompt}")
    for i, (label, desc) in enumerate(options, 1):
        marker = green("►") if i == default else " "
        print(f"  {marker} {i}. {bold(label)}{f'  — {dim(desc)}' if desc else ''}")
    while True:
        raw = _ask(f"Choice", str(default))
        if raw.isdigit() and 1 <= int(raw) <= len(options):
            return int(raw)
        print(yellow(f"  Please enter a number between 1 and {len(options)}."))


def _ok(msg: str):   print(f"  {green('✓')} {msg}")
def _warn(msg: str): print(f"  {yellow('⚠')} {msg}")
def _err(msg: str):  print(f"  \033[31m✗\033[0m {msg}" if _COLOUR else f"  ✗ {msg}")
def _hdr(msg: str):  print(f"\n{bold(cyan('── ' + msg + ' ──'))}")


def _local_ip() -> str:
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.connect(("8.8.8.8", 80))
        ip = s.getsockname()[0]
        s.close()
        return ip
    except Exception:
        return "?.?.?.?"


def _mdns_name() -> str:
    try:
        h = socket.gethostname()
        return h if h.endswith(".local") else h + ".local"
    except Exception:
        return ""


def _find_vaults(max_results: int = 8) -> list[Path]:
    """Walk common locations for .obsidian directories."""
    candidates = [
        Path.home() / "Documents",
        Path.home() / "Dropbox",
        Path.home() / "iCloud Drive",
        Path.home() / "OneDrive",
        Path.home(),
    ]
    vaults: list[Path] = []
    seen: set[Path] = set()
    for base in candidates:
        if not base.is_dir():
            continue
        try:
            for item in base.iterdir():
                if not item.is_dir():
                    continue
                obsidian = item / ".obsidian"
                if obsidian.is_dir() and item not in seen:
                    vaults.append(item)
                    seen.add(item)
                # One level deeper
                try:
                    for sub in item.iterdir():
                        if sub.is_dir() and (sub / ".obsidian").is_dir() and sub not in seen:
                            vaults.append(sub)
                            seen.add(sub)
                except PermissionError:
                    pass
        except PermissionError:
            pass
        if len(vaults) >= max_results:
            break
    return vaults[:max_results]


def _load_existing() -> dict[str, str]:
    """Load existing config from ~/.marginalia.env."""
    cfg: dict[str, str] = {}
    if CONFIG_FILE.exists():
        for line in CONFIG_FILE.read_text().splitlines():
            line = line.strip()
            if line.startswith("export ") and "=" in line:
                k, _, v = line[len("export "):].partition("=")
                cfg[k.strip()] = v.strip().strip('"').strip("'")
    return cfg


def _write_config(cfg: dict[str, str]) -> None:
    lines = [
        "# marginalia configuration — generated by 'marginalia setup'",
        "# source this file: set -a && source ~/.marginalia.env && set +a",
        "",
    ]
    for k, v in cfg.items():
        # Quote values that contain spaces
        val = f'"{v}"' if " " in v else v
        lines.append(f"export {k}={val}")
    CONFIG_FILE.write_text("\n".join(lines) + "\n")


# ── Provider setup ────────────────────────────────────────────────────────────

def _setup_openai(cfg: dict[str, str]) -> bool:
    existing = cfg.get("MARGINALIA_OPENAI_API_KEY", "")
    hint = f"(current: {existing[:10]}…)" if existing else "(starts with sk-...)"
    key = _ask(f"OpenAI API key {dim(hint)}", existing)
    if not key:
        _warn("Skipped — you can set MARGINALIA_OPENAI_API_KEY later.")
        return False
    print(f"  {dim('Testing key…')}", end="", flush=True)
    try:
        import openai
        client = openai.OpenAI(api_key=key)
        client.models.list()
        print(f"\r  {green('✓')} OpenAI key valid")
        cfg["MARGINALIA_OPENAI_API_KEY"] = key
        cfg.setdefault("MARGINALIA_MODEL_ID", "openai:gpt-4o")
        return True
    except ImportError:
        print()
        _err("openai package not installed. Run: pip install 'marginalia[openai]'")
        cfg["MARGINALIA_OPENAI_API_KEY"] = key  # save anyway
        return True
    except Exception as e:
        print()
        _err(f"Key rejected: {e}")
        return False


