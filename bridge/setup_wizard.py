def _setup_bedrock(cfg: dict[str, str]) -> bool:
    existing = cfg.get("MARGINALIA_AWS_PROFILE", "")
    profile = _ask("AWS profile name", existing or "default")
    print(f"  {dim('Testing credentials…')}", end="", flush=True)
    try:
        import boto3
        session = boto3.Session(profile_name=profile, region_name="us-west-2")
        sts = session.client("sts")
        sts.get_caller_identity()
        print(f"\r  {green('✓')} AWS credentials valid")
        cfg["MARGINALIA_AWS_PROFILE"] = profile
        cfg.setdefault("MARGINALIA_MODEL_ID", "us.anthropic.claude-sonnet-4-6")
        return True
    except ImportError:
        print()
        _err("boto3 not installed. Run: pip install 'marginalia[bedrock]'")
        cfg["MARGINALIA_AWS_PROFILE"] = profile
        return True
    except Exception as e:
        print()
        _err(f"Credentials failed: {e}")
        return False


# ── Vault setup ───────────────────────────────────────────────────────────────

def _setup_vault(cfg: dict[str, str]) -> None:
    vaults = _find_vaults()
    existing = cfg.get("MARGINALIA_VAULT", "")

    if vaults:
        opts = [(str(v), dim(str(v).replace(str(Path.home()), "~"))) for v in vaults]
        opts.append(("Enter path manually", ""))
        choice = _pick("Which Obsidian vault should book notes go to?", opts)
        if choice <= len(vaults):
            vault = str(vaults[choice - 1])
        else:
            vault = _ask("Vault path", existing or str(Path.home() / "Documents"))
            vault = os.path.expanduser(vault)
    else:
        _warn("No Obsidian vaults detected. Enter the path to your vault root.")
        vault = _ask("Vault path", existing or str(Path.home() / "Documents"))
        vault = os.path.expanduser(vault)

    cfg["MARGINALIA_VAULT"] = vault
    _ok(f"Vault: {vault}")


# ── Service install ───────────────────────────────────────────────────────────

def _install_macos(cfg: dict[str, str]) -> None:
    install_sh = BRIDGE_DIR / "install.sh"
    if not install_sh.exists():
        _warn("install.sh not found — skipping LaunchAgent setup.")
        return
    env = {**os.environ, **{k: v for k, v in cfg.items()}}
    result = subprocess.run(
        ["bash", str(install_sh)],
        env=env,
        cwd=str(BRIDGE_DIR),
    )
    if result.returncode != 0:
        _warn("LaunchAgent install reported an error — check the output above.")


def _install_linux(cfg: dict[str, str]) -> None:
    service_src = BRIDGE_DIR / "marginalia.service"
    service_dst = Path.home() / ".config" / "systemd" / "user" / "marginalia.service"
    if not service_src.exists():
        _warn("marginalia.service not found.")
        return
    service_dst.parent.mkdir(parents=True, exist_ok=True)
    # Build the service file with real values
    content = service_src.read_text()
    venv_python = sys.executable
    bridge_server = str(BRIDGE_DIR / "server.py")
    content = content.replace(
        "ExecStart=/usr/bin/python3 %h/marginalia/bridge/server.py",
        f"ExecStart={venv_python} {bridge_server}",
    ).replace(
        "WorkingDirectory=%h/marginalia/bridge",
        f"WorkingDirectory={BRIDGE_DIR}",
    )
    # Inject env vars
    env_lines = "\n".join(f"Environment={k}={v}" for k, v in cfg.items()
                           if k.startswith("MARGINALIA_"))
    content = content.replace(
        "Environment=MARGINALIA_MODEL_ID=openai:gpt-4o\n",
        env_lines + "\n",
    )
    service_dst.write_text(content)
    subprocess.run(["systemctl", "--user", "daemon-reload"], check=False)
    subprocess.run(["systemctl", "--user", "enable", "--now", "marginalia"], check=False)
    _ok(f"systemd service installed → {service_dst}")


def _setup_service(cfg: dict[str, str]) -> None:
    system = platform.system()
    if system == "Darwin":
        choice = _pick("Install as a background service?", [
            ("Yes — LaunchAgent (starts at login, restarts automatically)", ""),
            ("No — I'll run 'marginalia serve' manually", ""),
        ])
        if choice == 1:
            _install_macos(cfg)
    elif system == "Linux":
        choice = _pick("Install as a systemd user service?", [
            ("Yes — systemd (starts at login, restarts automatically)", ""),
            ("No — I'll run 'marginalia serve' manually", ""),
        ])
        if choice == 1:
            _install_linux(cfg)
    else:
        print(f"\n  {dim('Windows: run')} {bold('marginalia serve')} {dim('in a terminal.')}")
        print(f"  {dim('For auto-start, add it to Task Scheduler — see docs/setup.md.')}")


# ── KOReader instructions ──────────────────────────────────────────────────────

def _print_koreader_instructions() -> None:
    ip      = _local_ip()
    mdns    = _mdns_name()
    plugin  = BRIDGE_DIR.parent / "marginalia.koplugin"

    _hdr("KOReader plugin")
    print(f"""
  1. Copy the plugin to your device:
     {bold('ADB (Android/Boox):')}
       adb push {plugin} /sdcard/koreader/plugins/marginalia.koplugin

     {bold('File manager / MTP:')}  copy the {cyan('marginalia.koplugin/')} folder to
       your device's {cyan('koreader/plugins/')} directory.

  2. Restart KOReader.

  3. Go to {bold('top menu → Tools (wrench) → marginalia')} and set:
       {bold('Host')}: {green(ip)}  {dim(f'(or {mdns} if on the same Wi-Fi)')}
       {bold('Port')}: 7731

  4. Tap {bold('Test connection')} — you should see ✓ Connected.
    """)


# ── Main wizard ───────────────────────────────────────────────────────────────

def run() -> None:
    print(f"\n{bold(cyan('marginalia setup'))}")
    print(dim("  AI reading companion for KOReader\n"))

    existing = _load_existing()
    if existing:
        print(f"  Existing config found at {CONFIG_FILE}")
        choice = _pick("What would you like to do?", [
            ("Reconfigure", "change provider, vault, or service settings"),
            ("Keep existing config and just show KOReader instructions", ""),
            ("Exit", ""),
        ])
        if choice == 2:
            _print_koreader_instructions()
            return
        if choice == 3:
            return

    cfg = dict(existing)

    # ── Step 1: Provider ───────────────────────────────────────────────────
    _hdr("Step 1 — AI provider")

    provider_opts = [
        ("OpenAI",    "gpt-4o recommended — best quality, simplest setup"),
        ("Anthropic", "Claude — alternative to OpenAI"),
        ("Bedrock",   "AWS credentials required"),
        ("Skip",      "configure manually later"),
    ]
    provider = _pick("Which AI provider?", provider_opts)

    ok = False
    if provider == 1:
        ok = _setup_openai(cfg)
    elif provider == 2:
        ok = _setup_anthropic(cfg)
    elif provider == 3:
        ok = _setup_bedrock(cfg)
    else:
        _warn("Skipped — set MARGINALIA_OPENAI_API_KEY (or equivalent) before running.")

    if not ok and provider != 4:
        retry = _ask("Provider setup failed. Continue anyway? (y/n)", "n")
        if retry.lower() != "y":
            return

    # ── Step 2: Vault ─────────────────────────────────────────────────────
    _hdr("Step 2 — Obsidian vault")
    _setup_vault(cfg)

    # ── Step 3: Save config ───────────────────────────────────────────────
    _hdr("Step 3 — Save config")
    _write_config(cfg)
    _ok(f"Config written to {CONFIG_FILE}")
    print(f"  {dim('To load in a terminal session:')}")
    print(f"  {cyan('set -a && source ~/.marginalia.env && set +a')}\n")

    # ── Step 4: Service ───────────────────────────────────────────────────
    _hdr("Step 4 — Background service")
    _setup_service(cfg)

    # ── Step 5: KOReader ─────────────────────────────────────────────────
    _print_koreader_instructions()

    print(f"  {bold(green('Setup complete!'))} Run {bold('marginalia serve')} to start the bridge.\n")
